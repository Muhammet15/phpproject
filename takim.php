  <p class="mb-0">Kastamonuspor, 18 Mart 1950 tarihinde yeşil-siyah renkleriyle kurulmuştur. O dönem şartlarında Kastamonuspor’un amatör kümede yer almasını sağlayan kurucular; Sabahattin Çelengil, Hasan Çırakoğlu, Vecdi İlhan, Kuddusi Akay, Alaaddin Himmetoğlu, Muhtar Yücebıyık, Avni Özbenli ve Fikret Ünal'dır.</h5></p>

<h5><br><B>Türkiye Kupası</B></h5>

Kastamonuspor Türkiye'nin ulusal kupası olan Türkiye Kupası mücadelesine ilk olarak 1975-76 sezonunda katılmış, söz konusu yılda ilk turda karşılaştıkları Düzcespor'a 1-0 mağlup olarak kupaya ilk turda veda etmiştir. 1976-77 sezonunda kupada ilk turdan dahil olan Kastamonuspor, bir önceki yıl da karşılaştığı Düzcespor'a 3-1 kaybederek 1976-77 sezonunda da ilk turda kupaya veda etmiştir. Kastamonuspor birkaç yıllık ayrılığın ardından 1981-82 yılında ikinci kademeden dahil olduğu kupa mücadelesinde Çorumspor'a yenilerek elenmiştir. 1982-83 sezonunda da kupaya ikinci kademeden dahil olan Kastamonuspor, kendi evinde 0-0 berabere kaldığı Tokatspor'a deplasmanda 3-1 kaybederek kupaya veda etmiştir. 1985-86 sezonunda ilk turdan dahil oldukları kupada Ankara Demirspor'a 1-0 kaybederek birkez daha kupaya ilk maç sonrasında veda etmiştir. 1986-87 sezonunda ilk turda karşılaştıkları Merzifonspor'a karşı 3-0 üstünlük sağlayan Kastamonuspor tarihinde ilk defa kupada bir üst tura çıkma başarısını göstermiştir. İkinci turda karşılaştıkları Orduspor ile 0-0 berabere kalmaları sonucunda gerçekleştirilen penaltı atışları sonucunda rakibine 4-3 mağlup olan Kastamonuspor kupaya bu turda veda etmiştir.


<h5><br><B>Amblemi</B></h5>

<p>Kastamonuspor Kulübü’nün resmi nitelikte kullanmakta olduğu amblem, 1987 yılında Grafiker Ressam Celal Kırkbeşoğlu tarafından çizilmiştir. Kırkbeşlioğlu tarafından yönetime sunulan üç adet logodan bir tanesi yönetim kurulunun kendi içerisinde yaptığı toplantı sonucunda beğenilerek takımın logosu yapılmıştır. Bu amblem aynı zamanda, 1987 yılına kadar, KSK harflerinden oluşan logo benzeri tasarımlarla maçlara çıkan futbol takımının ilk takım amblemi olmuştur.
</p>
<h5><br><B>Renkleri</B></h5>

<p>Kastamonuspor kurulduğu ilk dönemlerde yeşil-siyah olarak belirlediği renkleri daha sonra kırmızı-siyah olarak değiştirmiştir. Kastamonuspor’un tarihi hakkında detaylı bilgilere sahip olan Yavuz Yaman konu hakkında şu açıklamalarda bulunmuştur: "Kastamonuspor’un amatör kümede yer aldığı dönemlerde renklerinin yeşil-siyahtı. Profesyonelliğe geçişin ve Çamsu Spor Kulübü ile birleşmemizin ardından renklerde değişiklik yapmak istenildi. O dönemin en ünlü ve başarılı takımı İtalyan devi Milan’dı. Kastamonuspor'un da Milan gibi başarılı olması isteğiyle renkleri kırmızı-siyah olan çizgili formalar yaptırılmıştır. Bu süreç sonunda Kastamonuspor’un rengini de kırmızı-siyah olarak belirlenmiştir."</p>
